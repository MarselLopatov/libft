#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			temp;
	unsigned int	nb;

	if (n < 0)
	{
		write(fd, "-", 1);
		nb = -n;
	}
	else
		nb = n;
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		temp = nb + '0';
		write(fd, &temp, 1);
	}
}
